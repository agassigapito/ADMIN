package com.telekommalaysia.portal.admin.constant;

public class CardDetails {

	private Long cardIdentificationNo;

	private String cardStatus;

	private String serviceStatus;

	public Long getCardIdentificationNo() {
		return cardIdentificationNo;
	}

	public void setCardIdentificationNo(Long cardIdentificationNo) {
		this.cardIdentificationNo = cardIdentificationNo;
	}

	public String getCardStatus() {
		return cardStatus;
	}

	public void setCardStatus(String cardStatus) {
		this.cardStatus = cardStatus;
	}

	public String getServiceStatus() {
		return serviceStatus;
	}

	public void setServiceStatus(String serviceStatus) {
		this.serviceStatus = serviceStatus;
	}

}
